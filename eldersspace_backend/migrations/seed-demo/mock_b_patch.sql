INSERT INTO points_transactions (user_id, source_type, points, type, created_at)
SELECT c.user_id, 'comment_activity', 5, 'earn', c.created_at
FROM comments c
WHERE c.post_id IN (SELECT post_id FROM mock_b_posts);

INSERT INTO comment_activity_rewards (
  user_id, phone_number, reward_date, comment_count, points_awarded, created_at
)
SELECT
  c.user_id,
  u.phone_number,
  c.created_at::date,
  COUNT(*)::int,
  (COUNT(*) * 5)::int,
  MIN(c.created_at)
FROM comments c
JOIN users u ON u.user_id = c.user_id
WHERE c.post_id IN (SELECT post_id FROM mock_b_posts)
GROUP BY c.user_id, u.phone_number, c.created_at::date
ON CONFLICT (user_id, reward_date) DO UPDATE
SET comment_count = EXCLUDED.comment_count,
    points_awarded = EXCLUDED.points_awarded,
    created_at = EXCLUDED.created_at;

UPDATE users u
SET total_points = COALESCE(t.total_earned, 0)
FROM (
  SELECT user_id, SUM(points) AS total_earned
  FROM points_transactions
  WHERE user_id IN (SELECT user_id FROM mock_b_users)
    AND type = 'earn'
  GROUP BY user_id
) t
WHERE u.user_id = t.user_id;

