WITH target_posts AS (
  SELECT p.post_id, p.user_id AS owner_id, p.content, p.created_at,
         row_number() OVER (ORDER BY p.post_id) AS prn
  FROM posts p
  WHERE p.is_deleted = 0
    AND NOT EXISTS (SELECT 1 FROM comments c WHERE c.post_id = p.post_id AND c.is_deleted = 0)
),
expanded AS (
  SELECT tp.*, 2 + ((tp.prn * 17) % 4) AS comment_count
  FROM target_posts tp
),
slots AS (
  SELECT e.post_id, e.owner_id, e.content, e.created_at, e.prn, gs AS k
  FROM expanded e, generate_series(1, e.comment_count) gs
),
assigned AS (
  SELECT s.*,
         1 + ((s.prn * 43 + s.k * 61) % (SELECT COUNT(*) FROM seed_commenters)) AS crn
  FROM slots s
),
commenter AS (
  SELECT a.*, sc.user_id AS commenter_id, sc.gender
  FROM assigned a
  JOIN seed_commenters sc ON sc.rn = a.crn
),
texts AS (
  SELECT
    c.*,
    CASE
      WHEN c.gender = 'ชาย' THEN
        COALESCE(m.male_comments[1 + ((c.k - 1) % GREATEST(array_length(m.male_comments, 1), 1))], 'ขอบคุณที่แบ่งปันครับ')
      ELSE
        COALESCE(m.female_comments[1 + ((c.k - 1) % GREATEST(array_length(m.female_comments, 1), 1))], 'ขอบคุณที่แบ่งปันค่ะ')
    END AS comment_text
  FROM commenter c
  LEFT JOIN content_comment_map m ON m.content = c.content
), inserted AS (
  INSERT INTO comments (post_id, user_id, content, is_deleted, parent_id, created_at, deleted_at, updated_at)
  SELECT
    t.post_id, t.commenter_id, t.comment_text, 0, NULL,
    LEAST(
      t.created_at + ((30 + ((t.k * 53 + t.prn) % 9000)) || ' minutes')::interval,
      CURRENT_TIMESTAMP - INTERVAL '1 minute'
    ),
    NULL, NULL
  FROM texts t
  RETURNING comment_id, user_id, created_at
)
SELECT * INTO TEMP seed_new_comments FROM inserted;

-- ---------------------------------------------------------------------
-- 3) คำนวณ points สำหรับ comment ใหม่ที่เพิ่งเพิ่ม (สอดคล้องกับระบบ
--    comment_activity เดิม — 5 แต้มต่อคอมเมนต์)
-- ---------------------------------------------------------------------
INSERT INTO points_transactions (user_id, source_type, points, type, created_at)
SELECT nc.user_id, 'comment_activity', 5, 'earn', nc.created_at
FROM seed_new_comments nc;

INSERT INTO comment_activity_rewards (
  user_id, phone_number, reward_date, comment_count, points_awarded, created_at
)
SELECT
  nc.user_id, u.phone_number, nc.created_at::date,
  COUNT(*)::int, (COUNT(*) * 5)::int, MIN(nc.created_at)
FROM seed_new_comments nc
JOIN users u ON u.user_id = nc.user_id
GROUP BY nc.user_id, u.phone_number, nc.created_at::date
ON CONFLICT (user_id, reward_date) DO UPDATE
SET comment_count = comment_activity_rewards.comment_count + EXCLUDED.comment_count,
    points_awarded = comment_activity_rewards.points_awarded + EXCLUDED.points_awarded;

UPDATE users u
SET total_points = COALESCE(t.total_earned, 0)
FROM (
  SELECT user_id, SUM(points) AS total_earned
  FROM points_transactions
  WHERE type = 'earn'
  GROUP BY user_id
) t
WHERE u.user_id = t.user_id
  AND u.user_id IN (SELECT DISTINCT user_id FROM seed_new_comments);

COMMIT;

