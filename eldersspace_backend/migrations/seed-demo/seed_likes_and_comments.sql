-- ---------------------------------------------------------------------
-- 1) เติม post_likes ให้โพสต์ที่ยังไม่มี like เลย (8-25 like ต่อโพสต์)
-- ---------------------------------------------------------------------
WITH target_posts AS (
  SELECT p.post_id, p.user_id AS owner_id,
         row_number() OVER (ORDER BY p.post_id) AS prn
  FROM posts p
  WHERE p.is_deleted = 0
    AND NOT EXISTS (SELECT 1 FROM post_likes l WHERE l.post_id = p.post_id)
),
liker_pool AS (
  SELECT row_number() OVER (ORDER BY user_id) AS urn, user_id
  FROM users
),
expanded AS (
  SELECT tp.post_id, tp.owner_id, tp.prn,
         8 + ((tp.prn * 13) % 18) AS like_count
  FROM target_posts tp
),
likes_gen AS (
  SELECT e.post_id, e.owner_id, e.prn, gs AS k
  FROM expanded e, generate_series(1, e.like_count) gs
),
assigned AS (
  SELECT lg.post_id, lg.owner_id, lg.k,
         1 + ((lg.prn * 37 + lg.k * 101) % (SELECT COUNT(*) FROM users)) AS urn
  FROM likes_gen lg
)
INSERT INTO post_likes (post_id, user_id, type, created_at)
SELECT a.post_id, lp.user_id, 'like',
       CURRENT_TIMESTAMP
         - (((a.post_id + lp.user_id) % 40) || ' days')::interval
         - (((lp.user_id * 7 + a.k) % 1440) || ' minutes')::interval
FROM assigned a
JOIN liker_pool lp ON lp.urn = a.urn
WHERE lp.user_id <> a.owner_id
ON CONFLICT (post_id, user_id) DO NOTHING;

